package game;

public class Speed {

	private int speedX = 0;
	private int speedY = 0;
	
	public void setSpeed(int x, int y){
		
		speedX = x;
		speedY = y;
		
	}
	
	public void setXSpeed(int x){
		
		speedX = x;
		
	}
	
	public void setYSpeed(int y){
		
		speedY = y;
		
	}
	
}
